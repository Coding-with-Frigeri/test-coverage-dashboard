function sum(a: number, b: number) {
  return a + b
}

function sub(a: number, b: number) {
  return a - b
}

function mul(a: number, b: number) {
  return a * b
}

function div(a: number, b: number) {
  return a / b
}

function mod(a: number, b: number) {
  return a % b
}

export { sum, sub, mul, div, mod }
